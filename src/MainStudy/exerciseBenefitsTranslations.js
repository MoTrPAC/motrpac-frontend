/**
 * Language configuration
 */
export const SUPPORTED_LANGUAGES = {
  en: { code: 'en', name: 'English', nativeName: 'English' },
  es: { code: 'es', name: 'Spanish', nativeName: 'Español' },
  fr: { code: 'fr', name: 'French', nativeName: 'Français' },
};

export const DEFAULT_LANGUAGE = 'en';

/**
 * Translation factory - retrieves translations for a given language
 * Falls back to English if the language is not supported
 * 
 * @param {string} lang - Language code (en, es, fr)
 * @returns {object} Translation object for the specified language
 */
export function getTranslations(lang) {
  const normalizedLang = lang?.toLowerCase();
  
  // Return the requested language if it exists, otherwise default to English
  if (normalizedLang && translations[normalizedLang]) {
    return translations[normalizedLang];
  }
  
  return translations[DEFAULT_LANGUAGE];
}

/**
 * Check if a language code is supported
 * 
 * @param {string} lang - Language code to check
 * @returns {boolean} True if the language is supported
 */
export function isLanguageSupported(lang) {
  if (!lang) return false;
  return Object.prototype.hasOwnProperty.call(SUPPORTED_LANGUAGES, lang.toLowerCase());
}

/**
 * Get the language code from URL parameters or default
 * 
 * @param {URLSearchParams} searchParams - URL search parameters
 * @returns {string} Valid language code
 */
export function getLanguageFromURL(searchParams) {
  const langParam = searchParams.get('lang');
  return isLanguageSupported(langParam) ? langParam.toLowerCase() : DEFAULT_LANGUAGE;
}
